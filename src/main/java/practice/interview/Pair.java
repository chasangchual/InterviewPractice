package practice.interview;

public class Pair {
    public Pair(final Integer first, final Integer second) {
        this.first = first;
        this.second = second;
    }
    Integer first;
    Integer second;

    public void setFirst(final Integer first) {
        this.first = first;
    }
    public void setSecond(final Integer second) {
        this.first = second;
    }
    public Integer getFirst() {
        return first;
    }

    public Integer getSecond() {
        return second;
    }
}
